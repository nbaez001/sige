	public void obtenerMotivo(String motivo, Boolean confirmacion,
			Integer tipoOperacion) {
		if (confirmacion) {
			grupo.setMotivoModificacion(motivo);
			this.confirmacion = true;
			if (tipoOperacion.equals(Constantes.TIPO_OPERACION.MODIFICACION)) {
				btnGuardar.click();
			} else {
				btnEliminar.click();
			}
			this.confirmacion = false;
		}
	}

	private void desactivarCampos(boolean estado) {
		txtDescripcion.setEnabled(estado);
	}

	@SuppressWarnings("unchecked")
	private void getAllGrupos() {
		IndexedContainer container = (IndexedContainer) tblGrupos
				.getContainerDataSource();
		container.removeAllItems();

		List<Grupo> gruposListado = null;
		Long paginaActual = Long.parseLong(this.txtPaginaActual.getValue().toString());
		Grupo grupoBuscar = new Grupo();
		grupoBuscar.setDescripcion(this.txtBuscarDescripcion.getValue().toString());
		Busqueda busqueda = grupoServicio.buscarPorGrupo(grupoBuscar,paginaActual);
		gruposListado = (List<Grupo>) busqueda.getRegistos();
		this.lblTotalPaginas.setValue(busqueda.getNumeroPaginas().toString());
		this.txtPaginaActual.setValue(busqueda.getPaginaActual().toString());

		Long numeroItem = (Long.parseLong(this.txtPaginaActual.getValue().toString()) - 1) * Constantes.PAGINACION.GRUPO + 1;
		for (int i = 0; i < gruposListado.size(); i++) {
			Item item = container.addItem(i);
			item.getItemProperty("id").setValue(gruposListado.get(i).getId());
			item.getItemProperty("item").setValue(numeroItem++);
			item.getItemProperty("descripcion").setValue(
					gruposListado.get(i).getDescripcion());
		}
	}

	private void getGrupo(Long grupoId) {
		grupo = grupoServicio.obtener(grupoId);
		this.txtDescripcion.setValue(grupo.getDescripcion().trim());
	}

	private void resetearFormulario() {
		this.flagEsNuevo = false;
		this.btnCancelar.setEnabled(false);
		this.btnNuevo.setEnabled(true);
		this.btnGuardar.setEnabled(false);
		this.btnEliminar.setEnabled(false);
		this.txtDescripcion.setValue("");
		desactivarCampos(false);
	}

	private void NuevoRegistro() {
		grupo = new Grupo();
		this.flagEsNuevo = true;
		this.btnGuardar.setCaption("Guardar");
		this.btnGuardar.setVisible(permiso.getNuevo());
		this.btnGuardar.setEnabled(true);
		this.btnCancelar.setVisible(permiso.getNuevo());
		this.btnCancelar.setEnabled(true);
		this.btnNuevo.setEnabled(false);
		this.btnEliminar.setEnabled(false);
		this.txtDescripcion.setValue("");
		desactivarCampos(permiso.getNuevo());
	}

	@Override
	public void buttonClick(ClickEvent event) {
		if (event.getSource() == this.btnNuevo) {
			NuevoRegistro();
			txtDescripcion.focus();
		} else {
			if (event.getSource() == this.btnGuardar) {
				if (SigeUtil.validarCamposTexto(txtDescripcion)) {
					grupo.setDescripcion(this.txtDescripcion.getValue()
							.toString());
					if (!grupoServicio.validarDuplicado(grupo)) {
						Notificacion.show(new Notificacion(
								"La descripcion de grupo ya existe",
								Constantes.MENSAJE.TYPE_ERROR));
						return;
					}
					if (this.flagEsNuevo) {
						grupoServicio.crear(grupo);
						Notificacion.show(new Notificacion(
								"Se guardo correctamente el grupo",
								Constantes.MENSAJE.TYPE_SUCCES));
					} else {
						if (!confirmacion) {
							Window ventana = SigeUtil.generarBuscador(
									new Window(), "INGRESAR MOTIVO", "360",
									"260");
							ventana.setContent(new PanelMotivoObservacion(
									ventana, this,
									Constantes.TIPO_OPERACION.MODIFICACION,
									(columnLenghts.get("motivomodifica"))));
							UI.getCurrent().addWindow(ventana);
							return;
						}
						grupoServicio.actualizar(grupo);
						Notificacion.show(new Notificacion(
								"Se actualizo correctamente el grupo",
								Constantes.MENSAJE.TYPE_SUCCES));
					}
					resetearFormulario();
					getAllGrupos();
				} else {
					Notificacion.show(new Notificacion(
							"Debe rellenar todos los campos",
							Constantes.MENSAJE.TYPE_ERROR));
				}
			} else {
				if (event.getSource() == this.btnCancelar) {
					resetearFormulario();
				} else {
					if (event.getSource() == this.btnBuscar) {
						this.txtPaginaActual.setValue("1");
						getAllGrupos();
					} else {
						if (event.getSource() == this.btnEliminar) {
							if (!confirmacion) {
								Window ventana = SigeUtil.generarBuscador(
										new Window(), "INGRESAR MOTIVO", "360",
										"260");
								ventana.setContent(new PanelMotivoObservacion(
										ventana, this,
										Constantes.TIPO_OPERACION.ELIMINACION,
										(columnLenghts.get("motivomodifica"))));
								UI.getCurrent().addWindow(ventana);
								return;
							}
							ConfirmDialog.show(UI.getCurrent(), "Confirmación",
									"¿Desea elminar el Grupo?", "Aceptar",
									"Cancelar", new ConfirmDialog.Listener() {
										private static final long serialVersionUID = 1L;

										public void onClose(ConfirmDialog dialog) {
											if (dialog.isConfirmed()) {
												grupoServicio
														.eliminiarGrupo(grupo);
												resetearFormulario();
												getAllGrupos();
											}
										}
									});
						} else {
							if (event.getSource() == this.btnSiguiente) {
								Long paginaActual = Long
										.parseLong(this.txtPaginaActual
												.getValue().toString()) + 1;
								if (paginaActual <= Long
										.parseLong(this.lblTotalPaginas
												.getValue().toString())) {
									this.txtPaginaActual.setValue(paginaActual
											.toString());
								}
								getAllGrupos();
							} else {
								if (event.getSource() == this.btnAtras) {
									Long paginaActual = Long
											.parseLong(this.txtPaginaActual
													.getValue().toString()) - 1;
									if (paginaActual >= 1) {
										this.txtPaginaActual
												.setValue(paginaActual
														.toString());
									}
									getAllGrupos();
								} else {
									if (event.getSource() == this.btnInicio) {
										this.txtPaginaActual.setValue("1");
										getAllGrupos();
									} else {
										if (event.getSource() == this.btnFin) {
											this.txtPaginaActual
													.setValue(this.lblTotalPaginas
															.getValue());
											getAllGrupos();
										} else if (event.getSource() == this.btnImprimir) {
											Window ventana = SigeUtil
													.generarBuscador(
															new Window(),
															"IMPRIMIR",
															"800px", "550px");
											Map<String, Object> parametros = new HashMap<String, Object>();
											parametros.put("descripcion",
													this.txtBuscarDescripcion
															.getValue()
															.toUpperCase());
											ventana.setContent(new PDFVizualizador(
													this, permiso, parametros,
													null, "GruposImprimir",
													null));
											UI.getCurrent().addWindow(ventana);
										}
									}
								}
							}
						}
					}
				}
			}
		}
	}

