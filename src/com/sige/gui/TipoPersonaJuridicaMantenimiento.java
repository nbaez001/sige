	public void obtenerMotivo(String motivo, Boolean confirmacion,
			Integer tipoOperacion) {
		if (confirmacion) {
			tipoPersonaJuridica.setMotivoModificacion(motivo);
			this.confirmacion = true;
			if (tipoOperacion.equals(Constantes.TIPO_OPERACION.MODIFICACION)) {
				btnGuardar.click();
			} else {
				btnEliminar.click();
			}
			this.confirmacion = false;
		}
	}

	public void getTipoPersonaJuridica(Long id) {
		tipoPersonaJuridica = tipoPersonaJuridicaService.obtener(id);
		txtNombre.setValue(tipoPersonaJuridica.getNombre().trim());
	}

	public void getAllTipoPersonaJuridica() {
		IndexedContainer container = (IndexedContainer) tbTipoPersonaJuridica
				.getContainerDataSource();
		container.removeAllItems();

		List<TipoPersonaJuridica> lsTipoPersonaJuridica = null;
		Long paginaActual = Long.parseLong(this.txtPaginaActual.getValue()
				.toString());
		TipoPersonaJuridica tipoPersonaJuridicaBuscar = new TipoPersonaJuridica();
		tipoPersonaJuridicaBuscar.setNombre(this.txtBuscarTipoPersonaJuridica
				.getValue().toString());
		Busqueda busqueda = tipoPersonaJuridicaService
				.buscarPorTipoPersonaJuridica(tipoPersonaJuridicaBuscar,
						paginaActual);
		lsTipoPersonaJuridica = (List<TipoPersonaJuridica>) busqueda
				.getRegistos();
		this.lblTotalPaginas.setValue(busqueda.getNumeroPaginas().toString());
		this.txtPaginaActual.setValue(busqueda.getPaginaActual().toString());

		Long numeroItem = (Long.parseLong(this.txtPaginaActual.getValue()
				.toString()) - 1)
				* Constantes.PAGINACION.TIPOPERSONAJURIDICA
				+ 1;
		for (int i = 0; i < lsTipoPersonaJuridica.size(); i++) {
			Item item = container.addItem(i);
			item.getItemProperty("id").setValue(
					lsTipoPersonaJuridica.get(i).getId());
			item.getItemProperty("item").setValue(numeroItem++);
			item.getItemProperty("nombre").setValue(
					lsTipoPersonaJuridica.get(i).getNombre());
		}
	}

	private void desactivarCampos(boolean estado) {
		this.txtNombre.setEnabled(estado);
	}

	private void resetearFormulario() {
		this.isNuevo = false;
		this.btnNuevo.setEnabled(true);
		this.btnCancelar.setEnabled(false);
		this.btnGuardar.setEnabled(false);
		this.btnEliminar.setEnabled(false);
		this.btnImprimir.setEnabled(true);
		txtNombre.setValue("");
		desactivarCampos(false);
	}

	private void NuevoRegistro() {
		tipoPersonaJuridica = new TipoPersonaJuridica();
		this.txtNombre.setValue("");
		this.isNuevo = true;
		this.btnCancelar.setVisible(permiso.getNuevo());
		this.btnCancelar.setEnabled(true);
		this.btnGuardar.setCaption("Guardar");
		this.btnGuardar.setVisible(permiso.getNuevo());
		this.btnGuardar.setEnabled(true);
		this.btnNuevo.setEnabled(false);
		desactivarCampos(permiso.getNuevo());
	}

	@Override
	public void buttonClick(ClickEvent event) {
		if (event.getSource() == this.btnNuevo) {
			NuevoRegistro();
			this.txtNombre.focus();
		} else if (event.getSource() == this.btnGuardar) {
			if (SigeUtil.validarCamposTexto(this.txtNombre)) {
				tipoPersonaJuridica.setNombre(this.txtNombre.getValue()
						.toString());
				tipoPersonaJuridica.setCodigoPersona(new Character('2'));
				if (!tipoPersonaJuridicaService
						.validarDuplicado(tipoPersonaJuridica)) {
					Notificacion.show(new Notificacion(
							"El tipo de persona juridica ya existe",
							Constantes.MENSAJE.TYPE_ERROR));
					return;
				}
				if (isNuevo) {
					tipoPersonaJuridicaService.crear(tipoPersonaJuridica);
					resetearFormulario();
					getAllTipoPersonaJuridica();
					Notificacion
							.show(new Notificacion(
									"Se guardo correctamente el tipo de persona juridica",
									Constantes.MENSAJE.TYPE_SUCCES));
				} else {
					if (!confirmacion) {
						Window ventana = SigeUtil.generarBuscador(new Window(),
								"INGRESAR MOTIVO", "360", "260");
						ventana.setContent(new PanelMotivoObservacion(ventana,
								this, Constantes.TIPO_OPERACION.MODIFICACION,
								(columnLenghts.get("motivomodifica"))));
						UI.getCurrent().addWindow(ventana);
						return;
					}
					tipoPersonaJuridicaService.actualizar(tipoPersonaJuridica);
					resetearFormulario();
					getAllTipoPersonaJuridica();
					Notificacion
							.show(new Notificacion(
									"Se actualizo correctamente el tipo de persona juridica",
									Constantes.MENSAJE.TYPE_SUCCES));
				}
			} else {
				Notificacion.show(new Notificacion(
						"Debe llenar todos los campos",
						Constantes.MENSAJE.TYPE_ERROR));
			}
		} else if (event.getSource() == this.btnInicio) {
			this.txtPaginaActual.setValue("1");
			getAllTipoPersonaJuridica();
		} else if (event.getSource() == this.btnSiguiente) {
			Long paginaActual = Long.parseLong(this.txtPaginaActual.getValue()
					.toString()) + 1;
			if (paginaActual <= Long.parseLong(this.lblTotalPaginas.getValue()
					.toString())) {
				this.txtPaginaActual.setValue(paginaActual.toString());
			}
			getAllTipoPersonaJuridica();
		} else if (event.getSource() == this.btnAtras) {
			Long paginaActual = Long.parseLong(this.txtPaginaActual.getValue()
					.toString()) - 1;
			if (paginaActual >= 1) {
				this.txtPaginaActual.setValue(paginaActual.toString());
			}
			getAllTipoPersonaJuridica();
		} else if (event.getSource() == this.btnFinal) {
			this.txtPaginaActual.setValue(this.lblTotalPaginas.getValue());
			getAllTipoPersonaJuridica();
		} else if (event.getSource() == this.btnConsultar) {
			this.txtPaginaActual.setValue("1");
			getAllTipoPersonaJuridica();
		} else if (event.getSource() == this.btnEliminar) {
			if (!confirmacion) {
				Window ventana = SigeUtil.generarBuscador(new Window(),
						"INGRESAR MOTIVO", "360", "260");
				ventana.setContent(new PanelMotivoObservacion(ventana, this,
						Constantes.TIPO_OPERACION.ELIMINACION, (columnLenghts
								.get("motivomodifica"))));
				UI.getCurrent().addWindow(ventana);
				return;
			}
			ConfirmDialog.show(UI.getCurrent(), "Confirmación",
					"¿Desea elminar el tipo de persona juridica?", "Aceptar",
					"Cancelar", new ConfirmDialog.Listener() {
						private static final long serialVersionUID = 1L;

						public void onClose(ConfirmDialog dialog) {
							if (dialog.isConfirmed()) {
								tipoPersonaJuridica.setEstado(Boolean.FALSE);
								tipoPersonaJuridicaService
										.actualizar(tipoPersonaJuridica);
								resetearFormulario();
								getAllTipoPersonaJuridica();
							}
						}
					});
		} else if (event.getSource() == this.btnCancelar) {
			resetearFormulario();
		} else if (event.getSource() == this.btnImprimir) {
			Window ventana = SigeUtil.generarBuscador(new Window(), "IMPRIMIR",
					"800px", "550px");
			Map<String, Object> parametros = new HashMap<String, Object>();
			parametros.put("nombre", this.txtBuscarTipoPersonaJuridica
					.getValue().toUpperCase());
			ventana.setContent(new PDFVizualizador(this, permiso, parametros,
					null, "TipoPersonaJuridicaImprimir", null));
			UI.getCurrent().addWindow(ventana);
		}
	}

